    // check cac gia tri duoc nhap vao form
    private boolean validateValueInForm() {
        // check null
        if (maPhiTxb.getText().trim().isEmpty()
                || tenPhiTxb.getText().trim().isEmpty()
                || mucPhiTxb.getText().trim().isEmpty()) {
            String message = "Vui lòng nhập hết các trường bắt buộc";
            JOptionPane.showMessageDialog(rootPane, message, "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        try {
            int d = Integer.parseInt(mucPhiTxb.getText());
            if (d < 0) {
                String message = "Số tiền phải lớn hơn 0";
                JOptionPane.showMessageDialog(rootPane, message, "Warning", JOptionPane.WARNING_MESSAGE);
                return false;
            }
        } catch (Exception e) {
            String message = "Mức phí không thể chứa các ký tự";
            JOptionPane.showMessageDialog(rootPane, message, "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (ngayBatDauChooser.getDate().after(ngayKetThucChooser.getDate())) {
            String message = "Ngày bắt đầu phải trước ngày kết thúc";
            JOptionPane.showMessageDialog(rootPane, message, "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        if (ngayBatDauChooser.getDate().before(new Date())) {
            String message = "Ngày bắt đầu không thể trước ngày hôm nay";
            JOptionPane.showMessageDialog(rootPane, message, "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        }
        
        return true;
    }


