    private void createBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createBtnActionPerformed
        if (validateValueInForm()) {
            KhoanPhiBatBuocModel temp = this.thuPhiBean.getKhoanPhiModel();

            temp.setMaPhi(maPhiTxb.getText());
            temp.setTenPhi(tenPhiTxb.getText());
            temp.setMucPhi(Integer.parseInt(mucPhiTxb.getText()));
            temp.setNgayBatDau(ngayBatDauChooser.getDate());
            temp.setNgayKetThuc(ngayKetThucChooser.getDate());

            try {
                if (this.controller.getThuPhiService().addNew(this.thuPhiBean.getKhoanPhiModel())) {
                    JOptionPane.showMessageDialog(null, "Thêm thành công!!");
                    close();
                    parentController.refreshData();
                }
            } catch (Exception e) {
                System.out.println(e.getMessage());
                JOptionPane.showMessageDialog(rootPane, "Có lỗi xảy ra. Vui long kiểm tra lại!!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_createBtnActionPerformed

